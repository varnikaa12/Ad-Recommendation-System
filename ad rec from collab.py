# neural network
class ClickingPrediction(nn.Module):
    def __init__(self, input_size=9):
        super(ClickingPrediction, self).__init__()
        self.fc1 = nn.Linear(input_size, 64)
        self.fc2 = nn.Linear(64, 32)
        self.fc3 = nn.Linear(32, 1)
        self.relu = nn.ReLU()
        self.sigmoid = nn.Sigmoid()
        self.dropout = nn.Dropout(0.2)

    def forward(self, x):
        x= self.relu(self.fc1(x))
        x= self.dropout(x)
        x= self.relu(self.fc2(x))
        x= self.sigmoid(self.fc3(x))
        return x

# training model
def train_model(model, X_train, y_train, epochs=20, learning_rate=0.001):
    criterion = nn.BCELoss()
    optimizer = optim.Adam(model.parameters(), lr=learning_rate)
    for epoch in range(epochs):
        model.train()
        optimizer.zero_grad()
        outputs = model(X_train)
        loss = criterion(outputs, y_train)
        loss.backward()
        optimizer.step()
        if (epoch+1) % 5 == 0:
            print(f'Epoch [{epoch+1}/{epochs}], Loss: {loss.item():.4f}')
    return model

# evaluate model
def evaluate_model(model, X_test, y_test):
    model.eval()
    with torch.no_grad():
        preds = model(X_test)
        preds_class = (preds > 0.5).float()
        accuracy = (preds_class == y_test).float().mean()
        print(f'Test Accuracy: {accuracy:.4f}')
    return preds.squeeze().numpy()

