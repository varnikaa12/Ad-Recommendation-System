        """
        Basic ACT-R inspired rule:
        - Higher relevance (Health, Travel) + Video Ads -> More likely click
        - Low CTR ads -> Less likely click
        """
        ad_type = features['Ad Type']
        ad_topic = features['Ad Topic']
        ctr = features['CTR']

        base_prob = 0.2  # defaulting to low probability

        if ad_topic in [0, 1]:
            base_prob += 0.3
        if ad_type == 1:  # video ad
            base_prob += 0.2
        if ctr > 0.05:
            base_prob += 0.1

        # simulate click decision
        return random.random() < base_prob

