# ACT-R
class ClickingModel:
    def __init__(self):
        self.thresholds = {
            'Health': 0.7,   # High interest
            'Travel': 0.65,  # High interest
            'Food': 0.6,     # Moderate interest
            'Tech': 0.55,    # Moderate interest
            'Fashion': 0.5,  # Low interest
            'Native': 0.45   # Low interest
        }

    def simulate_click(self, user_features, ad_features):
        """Simulate human-like click decisions based on ACT-R"""

        # base click probability
        base_prob = 0.2

        # adjustments- age
        age = user_features.get('Age', 30)
        if 18 <= age <= 30:
            # Young users more likely to click on ads
            base_prob += 0.1

        # adjustments- ad type
        ad_type = ad_features.get('Ad Type')
        if ad_type == 'Video':
            base_prob += 0.15  # Video ads get more attention
        elif ad_type == 'Banner':
            base_prob += 0.05  # Banner ads get some attention

        # ad topic
        ad_topic = ad_features.get('Ad Topic')
        topic_threshold = self.thresholds.get(ad_topic, 0.5)

        # adjustments- location & placement
        if ad_features.get('Ad Placement') == 'Social Media':
            base_prob += 0.1  # Social media gets more attention

        # adding a random factor for variability in regular human behavior
        random_factor = random.uniform(-0.1, 0.1)

        final_prob = base_prob + random_factor

        # decision making part
        return final_prob > topic_threshold

# ad recommendation
def recommend_ads_for_user(user_features, ad_pool, model, scaler, encoders, top_n=3):
    """Recommend ads for a specific user"""
    ad_predictions= []

    # for each potential ad in ad pool
    for ad in ad_pool:
        feature_vector = [
            user_features['Age'],
            user_features['Income'],
            user_features['Location'],
            encoders['Ad Type'].transform([ad['Ad Type']])[0],  # Ad Type
            encoders['Ad Topic'].transform([ad['Ad Topic']])[0],  # Ad Topic
            encoders['Ad Placement'].transform([ad['Ad Placement']])[0],  # Ad Placement
            ad['Clicks'],
            ad['Conversion Rate'],
            ad['CTR']
        ]

        # scale
        scaled_features = scaler.transform([feature_vector])
        features_tensor = torch.tensor(scaled_features, dtype=torch.float32)

        # click probability (prediction)
        with torch.no_grad():
            click_prob = model(features_tensor).item()
        ad_predictions.append((ad, click_prob))
    ranked_ads = sorted(ad_predictions, key=lambda x: x[1], reverse=True)

    # top n recommendations
    return ranked_ads[:top_n]



